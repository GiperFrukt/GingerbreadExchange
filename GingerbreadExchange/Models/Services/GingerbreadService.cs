using System;
using System.Collections;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace GingerbreadExchange.Models.Services
{
    /// <summary>
    /// Сервис взаимодействия модели продукта с БД
    /// </summary>
    public class GingerbreadService
    {
        public static IList QueryGingerbreads()
        {
            var result = SqlBuilder.Select<Gingerbread>().ToList();
            return result;
        }

        public static bool DeleteGingerbread(Gingerbread gb)
        {
            return SqlBuilder.Delete<Gingerbread>(gb);
        }

        public static bool AddGingerbread(Gingerbread gb)
        {
            return SqlBuilder.Add<Gingerbread>(gb);
        }

        public static bool UpdateGingerbread(Gingerbread gb)
        {
            return SqlBuilder.Update<Gingerbread>(gb);
        }
    }

    public class CurrencyService
    {
        public static IList QueryCurrency()
        {
            var result = SqlBuilder.Select<Currency>().ToList();
            return result;
        }

        public static bool UpdateGingerbread(Currency c)
        {
            return SqlBuilder.Update<Currency>(c);
        }
    }
}